const PythonAgentService = require('../services/pythonAgent.service');
const UserModel = require('../models/user.model');
const { supabaseAdmin } = require('../config/supabase');

/**
 * Generate documentation for a repository
 * POST /api/docs/generate
 * Body: { repo_name, doc_type? }
 */
async function generateDocumentation(req, res) {
    try {
        const { repo_name, doc_type = 'full', branch_filter = null } = req.body;
        const userId = req.userId;

        if (!repo_name) {
            return res.status(400).json({
                success: false,
                message: 'repo_name is required'
            });
        }

        // Get user details
        const user = await UserModel.findById(userId);
        if (!user) {
            return res.status(404).json({
                success: false,
                message: 'User not found'
            });
        }

        const fullUser = await UserModel.findByEmail(user.email);

        // Define prompts based on doc_type
        const prompts = {
            overview: `Generate a comprehensive project overview documentation for the repository "${repo_name}". Include: project purpose, main features, technology stack, architecture overview, and key dependencies. Format as structured Markdown with proper headings.`,

            setup: `Create a detailed setup and installation guide for the repository "${repo_name}". Include: prerequisites, environment setup, configuration steps, build commands, and run instructions. Format as step-by-step Markdown with code blocks.`,

            api: `Document the main code structure and API of the repository "${repo_name}". Include: key modules and their purposes, important functions/classes, data flow diagrams in text, and entry points. Use Markdown with code blocks for examples.`,

            full: `Generate complete project documentation for the repository "${repo_name}" with the following sections:
1. Project Overview (purpose, goals, target audience)
2. Architecture & Tech Stack (system design, technologies used)
3. Setup & Installation (prerequisites, steps, configuration)
4. Key Features (main functionality with examples)
5. Code Structure (folder organization, important files)
6. Usage Examples (common use cases with code)
7. Contributing Guidelines (how to contribute)

Format as professional Markdown with proper headings (#, ##, ###), lists, code blocks using triple backticks, and clear section separation. Make it ready for PDF export.`
        };

        const selectedPrompt = prompts[doc_type] || prompts.full;

        // Generate a title and summary on the fly
        const titleMap = {
            overview: `${repo_name} Overview`,
            setup: `${repo_name} Setup Guide`,
            api: `${repo_name} API Reference`,
            full: `${repo_name} Complete Documentation`
        };
        const title = titleMap[doc_type] || `${repo_name} Documentation`;
        const summary = `Automated ${doc_type} documentation generated by Infinium AI for the ${repo_name} repository.`;

        console.log(`📝 Generating ${doc_type} documentation for ${repo_name}...`);
        
        // Handle Streaming
        const isStream = req.query.stream === 'true';
        if (isStream) {
            res.setHeader('Content-Type', 'text/plain; charset=utf-8');
            res.setHeader('Transfer-Encoding', 'chunked');

            const stream = await PythonAgentService.streamQueryRepo(
                repo_name,
                selectedPrompt,
                null,
                branch_filter
            );

            let fullAnswer = "";
            let jsonBuffer = "";
            
            stream.on('data', (chunk) => {
                const text = chunk.toString();
                
                // If it looks like JSON or we are already buffering
                if (text.trim().startsWith('{') || jsonBuffer) {
                    jsonBuffer += text;
                    try {
                        // Try to see if we have a complete JSON object
                        const parsed = JSON.parse(jsonBuffer);
                        const content = parsed.answer || parsed.content;
                        if (content) {
                            // We found the full content! 
                            // Note: Documentation usually comes in one or a few big JSONs
                            fullAnswer = content;
                            res.write(content);
                            jsonBuffer = ""; // Reset buffer
                        }
                    } catch (e) {
                        // Not a complete JSON yet, keep buffering
                        // But also try regex for partial extraction to keep the UI alive
                        const match = jsonBuffer.match(/"answer"\s*:\s*"((?:[^"\\]|\\.)*)"/);
                        if (match && match[1]) {
                            try {
                                const partial = JSON.parse(`"${match[1]}"`);
                                // Only write the NEW part
                                const newPart = partial.substring(fullAnswer.length);
                                if (newPart) {
                                    fullAnswer += newPart;
                                    res.write(newPart);
                                }
                            } catch(err) {}
                        }
                    }
                } else {
                    // Raw text stream
                    fullAnswer += text;
                    res.write(text);
                }
            });

            stream.on('end', async () => {
                try {
                    // Post-process: Save to DB
                    const { data: existingDoc } = await supabaseAdmin
                        .from('documentation')
                        .select('id')
                        .eq('repo_name', repo_name)
                        .eq('doc_type', doc_type)
                        .eq('user_id', user.id)
                        .maybeSingle();

                    if (existingDoc) {
                        await supabaseAdmin.from('documentation').update({
                            content: fullAnswer,
                            updated_at: new Date().toISOString()
                        }).eq('id', existingDoc.id);
                    } else {
                        await supabaseAdmin.from('documentation').insert({
                            repo_name,
                            doc_type,
                            content: fullAnswer,
                            user_id: user.id,
                            updated_at: new Date().toISOString()
                        });
                    }
                } catch (e) {
                    console.error('Error storing streamed documentation:', e);
                }
                res.end();
            });

            stream.on('error', (err) => {
                console.error('Documentation stream error:', err);
                res.write(`\n⚠️ Error during generation: ${err.message}`);
                res.end();
            });

            return;
        }

        // --- NON-STREAMING (Legacy/Fallback) ---
        const response = await PythonAgentService.queryRepo(
            repo_name,
            selectedPrompt,
            null,
            branch_filter
        );

        // UPSERT: Check if documentation already exists
        const { data: existingDoc } = await supabaseAdmin
            .from('documentation')
            .select('id')
            .eq('repo_name', repo_name)
            .eq('doc_type', doc_type)
            .eq('user_id', user.id)
            .maybeSingle();

        let result;
        if (existingDoc) {
            // Update existing documentation
            const { data, error } = await supabaseAdmin
                .from('documentation')
                .update({
                    content: response.answer,
                    sources: response.sources || [],
                    model_used: response.model,
                    tokens_used: response.tokens_used,
                    updated_at: new Date().toISOString()
                })
                .eq('id', existingDoc.id)
                .select()
                .single();

            if (error) throw error;
            result = data;
            console.log(`📝 Updated existing ${doc_type} documentation for ${repo_name}`);
        } else {
            // Insert new documentation
            const { data, error } = await supabaseAdmin
                .from('documentation')
                .insert([{
                    user_id: user.id,
                    repo_name: repo_name,
                    doc_type: doc_type,
                    content: response.answer,
                    sources: response.sources || [],
                    model_used: response.model,
                    tokens_used: response.tokens_used
                }])
                .select()
                .single();

            if (error) throw error;
            result = data;
            console.log(`📝 Created new ${doc_type} documentation for ${repo_name}`);
        }

        res.json({
            success: true,
            data: {
                id: result.id,
                markdown: result.content,
                title: title,
                summary: summary,
                repo_name: repo_name,
                doc_type: doc_type,
                type: doc_type,
                sources: result.sources,
                model: result.model_used,
                tokens_used: result.tokens_used,
                created_at: result.created_at,
                updated_at: result.updated_at,
                is_new: !existingDoc
            }
        });

    } catch (error) {
        console.error('Documentation generation error:', error);
        res.status(500).json({
            success: false,
            message: error.message || 'Failed to generate documentation'
        });
    }
}

/**
 * Get all documentation for the authenticated user
 * GET /api/docs
 */
async function getUserDocs(req, res) {
    try {
        const userId = req.userId;
        
        const user = await UserModel.findById(userId);
        if (!user) {
            return res.status(404).json({
                success: false,
                message: 'User not found'
            });
        }
        
        const fullUser = await UserModel.findByEmail(user.email);
        
        const { data: docs, error } = await supabaseAdmin
            .from('documentation')
            .select('*')
            .eq('user_id', user.id)
            .order('updated_at', { ascending: false });
        
        if (error) throw error;

        // Map content to markdown and generate titles/summaries
        const mappedDocs = docs.map(d => {
            const titleMap = {
                overview: `${d.repo_name} Overview`,
                setup: `${d.repo_name} Setup Guide`,
                api: `${d.repo_name} API Reference`,
                full: `${d.repo_name} Complete Documentation`
            };
            return {
                ...d,
                markdown: d.content,
                title: titleMap[d.doc_type] || `${d.repo_name} Documentation`,
                summary: `Automated ${d.doc_type} documentation generated by Infinium AI.`,
                type: d.doc_type
            };
        });
        
        res.json({
            success: true,
            data: {
                documentation: mappedDocs,
                total: docs.length
            }
        });
        
    } catch (error) {
        console.error('Get user docs error:', error);
        res.status(500).json({
            success: false,
            message: 'Failed to get documentation'
        });
    }
}

